let sayi2 = 5
// operator=
const DORT_ISLEM = (operator) => {
    if (operator = "*") {
        sonuc = sayi1 * sayi2
    } else if (operator = "/") {
        sonuc = sayi1 / sayi2
    } else if (operator = "+") {
        sonuc = sayi1 + sayi2
    } else if (operator = "-") {
        sonuc = sayi1 - sayi2
    }
}
